    public void TampilData(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID PENGEMBALIAN");
        model.addColumn("ID PEMINJAMAN");
        model.addColumn("TANGGAL PENGEMBALIAN");
        model.addColumn("LAMA TERLAMBAT");
        model.addColumn("DENDA");
        
        try{
            String sql = "SELECT * FROM pengembalian";
            java.sql.Connection conn = connection.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while (res.next()){
                model.addRow(new Object[]{
                res.getString(1),
                res.getString(2),
                res.getString(3),
                res.getString(4),
                res.getString(5)});
                x = res.getRow()+1;
            }
            jTable1.setModel(model);
            
        }
        catch(SQLException e){
            System.out.println("Error "+e.getMessage());
        }
    }
    
    public void TampilDataPinjam(){
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID PINJAM");
        model.addColumn("NIM");
        model.addColumn("NAMA");
        model.addColumn("PRODI");
        model.addColumn("ID BUKU");
        model.addColumn("JUDUL");
        model.addColumn("TANGGAL PINJAM");
        model.addColumn("TANGGAL KEMBALI");
        model.addColumn("STATUS");
        
        try{
            String sql = "SELECT * FROM peminjaman where status = 'belum'";
            java.sql.Connection conn = connection.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while (res.next()){
                model.addRow(new Object[]{
                res.getString(1),
                res.getString(2),
                res.getString(3),
                res.getString(4),
                res.getString(5),
                res.getString(6),
                res.getString(7),
                res.getString(8),
                res.getString(9)});
            }
            jTable2.setModel(model);
            
        }
        catch(SQLException e){
            System.out.println("Error "+e.getMessage());
        }
    }


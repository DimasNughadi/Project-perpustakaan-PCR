        jTextField1.setText(id_peminjaman);
        String nim = jTable1.getValueAt(baris, 1).toString();
        jTextField2.setText(nim);
        tglpengembalian = jTable1.getValueAt(baris, 2).toString();
        jDateChooser1.getDate();
